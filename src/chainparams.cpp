#define ARRAY_PREALLOC_SIZE(arr) (sizeof(arr) / sizeof((arr)[0]))


double CoinFromAmount(const CAmount& amount)
{
    return (double)amount / (double)COIN;
}

static CBlock CreatePreAllocGenesisBlock(uint32_t nTime, uint32_t nNonce, uint32_t nBits, int32_t nVersion)
{
	const char* pszTimestamp = "The Source BlockChain, a leading economic collaboration platform.";
	CAmount amBaseAlloc = GENESIS_BASE_ALLOC * COIN;	//巨额基础预分配
	CAmount amTotalBatchAlloc = 0 * COIN;		//小额批量预分配 总量


	//计算小额批量预分配 总量
	for(int i = 0; i <ARRAY_PREALLOC_SIZE(arrPreAlloc); i++)
	{
		amTotalBatchAlloc += arrPreAlloc[i].amAlloc;
	}

	assert(amTotalBatchAlloc < amBaseAlloc * 0.83); //小额批量预分配 总量， 不超过 Base 的70%
	assert(ARRAY_PREALLOC_SIZE(arrPreAlloc));


	//巨额基础预分配, 扣除小额批量预分配
    CMutableTransaction txNew;
    txNew.nVersion = 1;
    txNew.vin.resize(1);
    txNew.vout.resize(1+ARRAY_PREALLOC_SIZE(arrPreAlloc));
    txNew.vin[0].scriptSig = CScript() << 486604799 << CScriptNum(4) << std::vector<unsigned char>((const unsigned char*)pszTimestamp, (const unsigned char*)pszTimestamp + strlen(pszTimestamp));
	txNew.vout[0].nValue = amBaseAlloc - amTotalBatchAlloc;
    txNew.vout[0].scriptPubKey =  CScript() << ParseHex(BASE_ALLOC_ADDR) << OP_CHECKSIG;

    //小额批量预分配
    for(int i = 0; i <ARRAY_PREALLOC_SIZE(arrPreAlloc); i++)
    {
    	txNew.vout[i+1].nValue =  arrPreAlloc[i].amAlloc;
        txNew.vout[i+1].scriptPubKey =  CScript() << ParseHex(arrPreAlloc[i].ptrScriptPubKey) << OP_CHECKSIG;
    }

    CBlock genesis;
    genesis.nTime    = nTime;
    genesis.nBits    = nBits;
    genesis.nNonce   = nNonce;
    genesis.nVersion = nVersion;
    genesis.hashPrevBlock.SetNull();

    genesis.vtx.push_back(MakeTransactionRef(std::move(txNew)));

    genesis.hashMerkleRoot = BlockMerkleRoot(genesis);

//	fprintf(stderr,	"%s %d|genesis create ok|amBaseAlloc=%f|amTotalBatchAlloc=%f|batAllocPercent=%f%%|BatchSize=%d|\n", __FILE__, __LINE__,
//			CoinFromAmount(amBaseAlloc), CoinFromAmount(amTotalBatchAlloc), CoinFromAmount(amTotalBatchAlloc)/CoinFromAmount(amBaseAlloc) *100.0, ARRAY_PREALLOC_SIZE(arrPreAlloc));

    return genesis;
}



/**
 * Build the genesis block. Note that the output of its generation
 * transaction cannot be spent since it did not originally exist in the
 * database.
 *
 * CBlock(hash=000000000019d6, ver=1, hashPrevBlock=00000000000000, hashMerkleRoot=4a5e1e, nTime=1231006505, nBits=1d00ffff, nNonce=2083236893, vtx=1)
 *   CTransaction(hash=4a5e1e, ver=1, vin.size=1, vout.size=1, nLockTime=0)
 *     CTxIn(COutPoint(000000, -1), coinbase 04ffff001d0104455468652054696d65732030332f4a616e2f32303039204368616e63656c6c6f72206f6e206272696e6b206f66207365636f6e64206261696c6f757420666f722062616e6b73)
 *     CTxOut(nValue=50.00000000, scriptPubKey=0x5F1DF16B2B704C8A578D0B)
 *   vMerkleTree: 4a5e1e
 */
static CBlock CreateGenesisBlock(uint32_t nTime, uint32_t nNonce, uint32_t nBits, int32_t nVersion, const CAmount& genesisReward)
{
    const char* pszTimestamp = "The Source BlockChain, a leading economic collaboration platform.";
    const CScript genesisOutputScript = CScript() << ParseHex("04678afdb0fe5548271967f1a67130b7105cd6a828e03909a67962e0ea1f61deb649f6bc3f4cef38c4f35504e51ec112de5c384df7ba0b8d578a4c702b6bf11d5f") << OP_CHECKSIG;
    return CreateGenesisBlock(pszTimestamp, genesisOutputScript, nTime, nNonce, nBits, nVersion, genesisReward);
}

